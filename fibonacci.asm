
SECTION TEXT
;Comentario
		COPY ZERO, OLDER
		COPY ONE, OLD
		INPUT LIMIT ;comentario 
		OUTPUT OLD
FRONT:	LOAD OLDER ;comentario 
		ADD OLD
		STORE NEW
		SUB LIMIT
		JMPP FINAL
		OUTPUT NEW
		COPY OLD, OLDER
		COPY NEW, OLD
		JMP FRONT
FINAL:	OUTPUT LIMIT ;comentario 
		STOP
		
SECTION DATA
ZERO: CONST 0
ONE: CONST 1
OLDER: SPACE
OLD: SPACE
NEW: SPACE
LIMIT: SPACE
